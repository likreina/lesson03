package com.company;

public class Calculator {

    public int add(int a, int b) {
        return a+b;
    }


    public int minus(int a, int b) {
        return a-b;
    }


    public long multiply(int a, int b) {
        long multiplied = (long) a*b;
        return multiplied;
    }

    public double division(int a, int b) {
        double divided = (double) a/b;
        return divided;
    }

}

